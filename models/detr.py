class SwinDetr(nn.Module):
    def __init__(self, num_classes, hidden_dim=256, nheads=8, num_encoder_layers=6, num_decoder_layers=6):
        super(SwinDetr, self).__init__()
        
        #create SwinT backbone
        self.backbone = nn.Sequential(
            *list(Swin_T(num_classes=num_classes, drop_path_rate=0.2).children())
        )[:-2]
        
        #create conversion layer
        self.conv = nn.Conv2d(8*96, hidden_dim, 1)
        
        #create a default PyTorch transformer
        self.transformer = nn.Transformer(hidden_dim, nheads, num_encoder_layers, num_decoder_layers)
        
        #prediction heads
        self.linear_class = nn.Linear(hidden_dim, num_classes + 1)
        self.linear_bbox = MLP(hidden_dim, hidden_dim, 4, 3)
        
        #object queries
        self.query_pos = nn.Parameter(torch.rand(100, hidden_dim))
        
        #positional embedding
        #self.input_proj = nn.Conv2d(backbone.num_channels, hidden_dim, kernel_size=1)
        self.row_embed = nn.Parameter(torch.rand(50, hidden_dim//2))
        self.col_embed = nn.Parameter(torch.rand(50, hidden_dim//2))
        
    def forward(self, inputs):
        _, _, H, W = inputs.size()
        if W % 4 != 0:
            inputs = F.pad(inputs, (0, 4 - W % 4))
        if H % 4 != 0:
            inputs = F.pad(inputs, (0, 0, 0, 4 - H % 4))
        x = self.backbone[0](inputs)
        #padding before patch merging
        _, H, W, _ = x.size()
        pad_input = (H % 2 == 1) or (W % 2 == 1)
        if pad_input:
            x = F.pad(x, (0, 0, 0, W % 2, 0, H % 2))
        x = self.backbone[1](x)
        #padding before patch merging
        _, H, W, _ = x.size()
        pad_input = (H % 2 == 1) or (W % 2 == 1)
        if pad_input:
            x = F.pad(x, (0, 0, 0, W % 2, 0, H % 2))
        x = self.backbone[2](x)
        #padding before patch merging
        _, H, W, _ = x.size()
        pad_input = (H % 2 == 1) or (W % 2 == 1)
        if pad_input:
            x = F.pad(x, (0, 0, 0, W % 2, 0, H % 2))
        x = self.backbone[3](x)
        x = self.backbone[4](x)
        x = rearrange(x, 'b h w c -> b c h w')
        h = self.conv(x)
        
        #construct positional embedding
        H, W = h.shape[-2:]
        B = h.shape[0]
        
        pos = torch.cat([
            self.col_embed[:W].unsqueeze(0).repeat(H, 1, 1),
            self.row_embed[:H].unsqueeze(1).repeat(1, W, 1),
        ], dim=-1).flatten(0, 1).unsqueeze(1).repeat(1, B, 1)
        
        
        #propagate through the transformer
        h = self.transformer(pos + 0.1 * h.flatten(2).permute(2, 0, 1),
                             self.query_pos.unsqueeze(1).repeat(1, B, 1)).transpose(0, 1)
        # finally project transformer outputs to class labels and bounding boxes
        #return h
        return {'pred_logits': self.linear_class(h), 
                'pred_boxes': self.linear_bbox(h).sigmoid()}


